import pandas as pd
from scipy import stats
from itertools import combinations

DATAFRAME_CACHE = "dataset/contrast_set_size.pkl"
CONDITIONS = ["Image-to-Text", "Composite", "Verbose", "Original"]
CONTRAST_SIZES = [3, 10, 25, 50, 100, 250, 500, 1000, 2500]
FULL_CONTRAST_SIZE = 4999

df = pd.read_pickle(DATAFRAME_CACHE)
rank_cols = [f"avg_rank_{n}" for n in CONTRAST_SIZES] + [f"rank_{FULL_CONTRAST_SIZE}"]
col_labels = [f"N={n}" for n in CONTRAST_SIZES] + [f"N={FULL_CONTRAST_SIZE}"]

for ref, comp in combinations(CONDITIONS, 2):
    print(f"\n{comp} vs {ref}:")
    ref_df = df[df["condition"] == ref]
    comp_df = df[df["condition"] == comp]

    for col, label in zip(rank_cols, col_labels):
        ref_vals = ref_df[col].values
        comp_vals = comp_df[col].values
        beta = comp_vals.mean() - ref_vals.mean()
        t_stat, p_value = stats.ttest_ind(comp_vals, ref_vals)
        df_resid = len(ref_vals) + len(comp_vals) - 2

        if p_value < 0.001:
            print(f"  {label:>8}: β = {beta:>7.3f}, t({df_resid})={t_stat:>7.2f}, p < 0.001")
        else:
            print(f"  {label:>8}: β = {beta:>7.3f}, t({df_resid})={t_stat:>7.2f}, p = {p_value:.4f}")

